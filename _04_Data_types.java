
public class _04_Data_types {
  public static void main(String[] args) {
    short s = 654;
    byte by = 32;
    int i = 32;
    boolean b = true;
    char ch = 'A';
    float f = 4.5f;
    double d = 5.83;   // 5.83d is also valid and it is the default floating value 
    String str ="Hey Divya";
    System.out.println(s + "\n" + by + "\n" + i + "\n" + b + "\n" + ch + "\n" + f + "\n" + d + "\n" + str);
  
  }
}
